package com.example.sanauno.sng;

public class WorkInfo {

    private String  category, title, subject, show;
    private int item, score, instructorClassID, studentLrn;

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getShow() {
        return show;
    }

    public void setShow(String show) {
        this.show = show;
    }

    public int getItem() {
        return item;
    }

    public void setItem(int item) {
        this.item = item;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getInstructorClassID() {
        return instructorClassID;
    }

    public void setInstructorClassID(int instructorClassID) {
        this.instructorClassID = instructorClassID;
    }

    public int getStudentLrn() {
        return studentLrn;
    }

    public void setStudentLrn(int studentLrn) {
        this.studentLrn = studentLrn;
    }
}
